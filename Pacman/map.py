class Map:

    def __init__(self):
        pygame.init()
        self.colliders = []

    def drawMap(self, screen):
        debugSize = 25
        posX = 0
        posY = 0
        for i in range(len(lvlMap)):
            for j in range(len(lvlMap[i])):
                if lvlMap[i][j] == 1:
                    surf = pygame.Surface((debugSize, debugSize))
                    rect = surf.get_rect(topleft=(posX, posY))
                    self.colliders.append(rect)
                    surf.fill(globals.COLOR_WHITE)
                    screen.blit(surf, (posX, posY))
                posX += debugSize
            posY += debugSize
            posX = 0

